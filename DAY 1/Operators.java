public class Operators {
    public static void main(String[] args) {
        int a,b,c,d,e,f,g,h,i,j,k;
        // arithmetic operators
        a=4;
        b=5;
        c=a+b;
        d=a-b;
        e=a*b;
        f=a/b;
        g=a%b;
        // logical operators
        // unary operators
        h=a++;
        i=a--;
        j=++a;
        k=--a;
        System.out.println(a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+g+" "+h+" "+i+" "+j+" "+k);

    }
}
